    private void btnApagarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApagarClienteActionPerformed
        boolean sucesso;
        ClienteControle clienteControle = new ClienteControle();
        try {
            sucesso = clienteControle.apagarCliente(this.codCliente);
            if(sucesso){
                JOptionPane.showMessageDialog(null, "O cliente foi apagado com sucesso");
                this.limparTela();
            }else{
                JOptionPane.showMessageDialog(null, "O cliente não foi apagado, selecione um cliente para apagar");
            }
        } catch (ExceptionDAO ex) {
            Logger.getLogger(TelaCadastroCliente.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(TelaCadastroCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnApagarClienteActionPerformed

