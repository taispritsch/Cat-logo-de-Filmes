    private void btnSalvarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarClienteActionPerformed
        boolean sucesso = false;
        
        try{
            ClienteControle clienteControle = new ClienteControle();
            
            if(this.codCliente == 0)
            {
                sucesso = clienteControle.cadastrarCliente(nomeCliente.getText(), cpfCliente.getText(), emailCliente.getText());
            }else{
                sucesso = clienteControle.alterarCliente(this.codCliente, nomeCliente.getText(), cpfCliente.getText(), emailCliente.getText());

            }
            
            if(sucesso){
                JOptionPane.showMessageDialog(null, "Cliente cadastrado com sucesso!");
               // this.limparTelaCadastro(evt);
            }else{
                if(nomeCliente.getText().equals("")){
                    JOptionPane.showMessageDialog(null, "O campo Nome é obrigatório. Favor preencher.", "Aviso!", JOptionPane.WARNING_MESSAGE);
                    return;
                }
                if(cpfCliente.getText().equals("")){
                    JOptionPane.showMessageDialog(null, "O campo CPF é obrigatório. Favor preencher.", "Aviso!", JOptionPane.WARNING_MESSAGE);
                    return;
                }
                
                JOptionPane.showMessageDialog(null, "CPF inválido. Por favor, verifique o seu Cpf.", "Aviso!", JOptionPane.WARNING_MESSAGE);
                nomeCliente.setText("");
                cpfCliente.setText("");
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "ERRO" + ex);
        }
        
        limparTela();
    }//GEN-LAST:event_btnSalvarClienteActionPerformed

